#custom pytorch dataset class

class NewsDataset(Dataset):
  """
  PyTorch Dataset wrapper for tokenized news articles.
  Works for both binary (bias) and multi-class(sentiment) tasks.
  """
  def __init__(self, texts, labels, tokenizer, max_length=256):
        self.texts = texts
        self.labels = labels
        self.tokenizer = tokenizer
        self.max_length = max_length

  def __len__(self):
    return len(self.texts)

  def __getitem__(self, idx):
    encoding = self.tokenizer(
        self.texts[idx],
        truncation=True,
        padding='max_length',
        max_length=self.max_length,
        return_tensors='pt'
    )
    return{
        'input_ids': encoding['input_ids'].squeeze(0),
        'attention_mask': encoding['attention_mask'].squeeze(0),
        'labels': torch.tensor(self.labels[idx], dtype=torch.long)
    }
print('NewDataset class defined.')

#Training and evaluation helper functions

def train_epoch(model, loader, optimizer, scheduler):
  """Run one full training epoch."""
  model.train()
  total_loss = 0
  for batch in loader:
    optimizer.zero_grad()
    input_ids = batch['input_ids'].to(device)
    attention_mask = batch['attention_mask'].to(device)
    labels = batch['labels'].to(device)

    outputs = model(input_ids, attention_mask=attention_mask, labels=labels)
    loss = outputs.loss
    total_loss += loss.item()

    loss.backward()
    torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
    optimizer.step()
    scheduler.step()

  return total_loss / len(loader)

def evaluate(model, data_loader, label_names):
    model.eval()
    all_preds = []
    all_labels = []
    
    with torch.no_grad():
        for batch in data_loader:
            input_ids = batch['input_ids'].to(device)
            attention_mask = batch['attention_mask'].to(device)
            labels = batch['labels'].to(device)
            
            outputs = model(input_ids, attention_mask=attention_mask)
            preds = torch.argmax(outputs.logits, dim=1)
            
            all_preds.extend(preds.cpu().numpy())
            all_labels.extend(labels.cpu().numpy())
            
    # Find unique classes actually present in the true labels or predictions
    unique_classes = np.unique(np.concatenate([all_labels, all_preds]))
    
    # Safely filter the target names array to match only the present active classes
    filtered_label_names = [label_names[i] for i in unique_classes if i < len(label_names)]
    
    # Print report matching exactly the dimensions present
    print(classification_report(all_labels, all_preds, labels=unique_classes, target_names=filtered_label_names))
    return all_preds, all_labels

def build_loaders(texts_train, texts_test, y_train, y_test, tokenizer, batch_size=16, max_length=256):
  """Tokenize splits and return DataLoaders."""
  train_dataset = NewsDataset(texts_train.tolist(), y_train.tolist(), tokenizer, max_length)
  test_dataset = NewsDataset(texts_test.tolist(), y_test.tolist(), tokenizer, max_length)

  train_loader = DataLoader(train_dataset, batch_size=batch_size, shuffle=True)
  test_loader = DataLoader(test_dataset, batch_size=batch_size, shuffle=False)
  return train_loader, test_loader

def fine_tune_bert(task_name, texts, labels, num_labels, label_names, epochs=3, batch_size=16, max_length=256, lr=2e-5):
  """
  Full BERT fine-tuning pipeline for a single classification task.
  Returns the trained model and tokenizer.
  """
  print(f'\n{"="*70}')
  print(f'Fine-Tuning BERT for {task_name.upper()}...')
  print(f'{"="*70}')

  # 1. Load tokenizer and model
  tokenizer = BertTokenizer.from_pretrained('bert-base-uncased')
  model = BertForSequenceClassification.from_pretrained('bert-base-uncased', num_labels=num_labels).to(device)

  # 2. Train/test split
  x_train, x_test, y_train, y_test = train_test_split(texts, labels, test_size=0.2, random_state=42, stratify=labels)
  print(f'Train: {len(x_train)}| Test: {len(x_test)}')

  # 3. Build DataLoaders
  train_loader, test_loader = build_loaders(x_train, x_test, y_train, y_test, tokenizer, batch_size, max_length)

  # 4. Set up optimizer and scheduler
  optimizer = AdamW(model.parameters(), lr=lr, weight_decay=0.01)
  total_steps = len(train_loader) * epochs
  scheduler = get_linear_schedule_with_warmup(optimizer, num_warmup_steps=int(0.1 * total_steps), num_training_steps=total_steps)

  # 5. Training loop
  for epoch in range(1, epochs + 1):
    avg_loss = train_epoch(model, train_loader, optimizer, scheduler)
    print(f'Epoch {epoch}/{epochs} | Average Loss: {avg_loss:.4f}')

  # 6. Final evaluation
  print(f'\n-- {task_name} Test Set Results ---')
  preds, true = evaluate(model, test_loader, label_names)
  return model, tokenizer, x_test, y_test, preds

