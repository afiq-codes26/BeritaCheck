import requests
from bs4 import BeautifulSoup
import pandas as pd
import time

def scrape_malaysian_news(url, outlet, index, total):
    headers = {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36'
    }
    print(f"🔄 [{index}/{total}] Scraping {outlet}...")
    try:
        if pd.isna(url) or str(url).strip() == "" or "http" not in str(url):
            return "No URL provided"

        response = requests.get(url, headers=headers, timeout=10)
        if response.status_code != 200:
            return f"Error: HTTP {response.status_code}"

        soup = BeautifulSoup(response.text, 'html.parser')
        paragraphs = soup.find_all('p')

        clean_paragraphs = [p.text.strip() for p in paragraphs if len(p.text.strip()) > 20]
        full_text = " ".join(clean_paragraphs)

        if len(full_text) > 0:
            print(f"✅ Success! Captured {len(clean_paragraphs)} paragraphs.")
        else:
            print(f"⚠️ Warning: No valid paragraph text found.")
        return full_text
    except Exception as e:
        return f"Scraping failed: {str(e)}"

