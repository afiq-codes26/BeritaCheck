def calculate_political_density(text):
    text_lower = str(text).lower()
    words = text_lower.split()
    if not words:
        return 0.0
    # Count how many political anchors appear in the text
    match_count = sum(1 for word in words if any(kw in word for kw in political_keywords))
    return match_count / len(words)

